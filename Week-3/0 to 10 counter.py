# 0 --> 10 using while loop
counter = 0
while counter <= 10:
    print(counter)
    counter += 1

# 10 --> 0 using while loop
counter = 10
while counter >= 0:
    print(counter)
    counter += -1

# 0 --> 10 using for loop
for num in range(0,11):
    print(num)

# 10 --> 0 using for loop
for num in range (10,-1,-1):
    print(num)